<script>
import { mapGetters } from 'vuex';
import { SERVICE } from '@/store/modules/service/actions';

function asyncFunction (item, cb) {
  setTimeout(() => {
    cb();
  }, 100);
}

export default {
	props: {
		updateService: {
      		type: Function,
      		default: () => {},
    	},
		service: Object,
		readOnly: {
      		type: Boolean,
      		default: false,
    	},
	},
	data(){
		return {
			"upper": false,
			"upper_right": false,
			"upper_left": false,
			"lower": false,
			"lower_right": false,
			"lower_left": false,
			"selectedTeeth": [],
		}
	},
	created() {
		if(this.service && this.service.service_data && this.service.service_data.implant) {
			this.upper = this.service.service_data.implant.upper
			this.upper_right = this.service.service_data.implant.upper_right
			this.upper_left = this.service.service_data.implant.upper_left
			this.lower = this.service.service_data.implant.lower
			this.lower_right = this.service.service_data.implant.lower_right
			this.lower_left = this.service.service_data.implant.lower_left
			this.selectedTeeth = 'selected_teeth' in  this.service.service_data.implant ? this.service.service_data.implant.selected_teeth : []
		}
	},
	watch:{
		upper(){
			this.updateTeethSelection();
		},
		upper_right(){
			this.updateTeethSelection();
		},
		upper_left(){
			this.updateTeethSelection();
		},
		lower(){
			this.updateTeethSelection();
		},
		lower_right(){
			this.updateTeethSelection();
		},
		lower_left(){
			this.updateTeethSelection();
		},
		selectedTeeth() {
			if(this.readOnly && this.selectedTeeth.length > 0) {
				this.selectedTeeth.forEach((teeth) => {
					document.querySelectorAll(`.${teeth}`).forEach((item)=>{
						item.classList.add('checked')
					});
				})
			}
		}
	},
	methods: {
		teeth: function () {
			if(!this.readOnly) {
				let customPath = document.querySelectorAll('.customPath');
				customPath.forEach((item) => {
					item.addEventListener('click', () => {
						item.classList.toggle('checked');
						this.updateImplant();
					});
				});
			}
		},
		updateImplant(){
			let data = {
				"upper": this.upper,
				"upper_right": this.upper_right,
				"upper_left": this.upper_left,
				"lower": this.lower,
				"lower_right": this.lower_right,
				"lower_left": this.lower_left,
				"teeth": [],
				"selected_teeth": this.selectedTeeth,
			}
			let teeth = [];
			var itemsProcessed = 0;
			let allTeeth = document.querySelectorAll('.customPath');
			allTeeth.forEach((item) => {
				asyncFunction(item, () => {
					itemsProcessed++;
					teeth.push(
						item.dataset.code,
					)
					if(itemsProcessed === allTeeth.length) {
						data["teeth"] = teeth;
						this.updateService({"implant": data})
					}
				});
			});
		},
		updateTeethSelection: function(){
			if (this.upper) {
				document.querySelectorAll('.isUpper').forEach((item)=>{
					item.classList.add('checked')
				});
			} else {
				document.querySelectorAll('.isUpper').forEach((item)=>{
					item.classList.remove('checked')
				});
				if (this.upper_right) {
					document.querySelectorAll('.isUpperRight').forEach((item)=>{
						item.classList.add('checked')
					});
				} else {
					document.querySelectorAll('.isUpperRight').forEach((item)=>{
						item.classList.remove('checked')
					});
				};
				if (this.upper_left) {
					document.querySelectorAll('.isUpperLeft').forEach((item)=>{
						item.classList.add('checked')
					});
				} else {
					document.querySelectorAll('.isUpperLeft').forEach((item)=>{
						item.classList.remove('checked')
					});
				};
			};
			if (this.lower) {
				document.querySelectorAll('.isLower').forEach((item)=>{
					item.classList.add('checked')
				});
			} else {
				document.querySelectorAll('.isLower').forEach((item)=>{
					item.classList.remove('checked')
				});
				if (this.lower_right) {
					document.querySelectorAll('.isLowerRight').forEach((item)=>{
						item.classList.add('checked')
					});
				} else {
					document.querySelectorAll('.isLowerRight').forEach((item)=>{
						item.classList.remove('checked')
					});
				};
				if (this.lower_left) {
					document.querySelectorAll('.isLowerLeft').forEach((item)=>{
						item.classList.add('checked')
					});
				} else {
					document.querySelectorAll('.isLowerLeft').forEach((item)=>{
						item.classList.remove('checked')
					});
				};
			};
			
			this.updateImplant();
		},
		selectOneTeeth(id) {
			let index  = this.selectedTeeth.findIndex((item) => item == id);
			if(index == -1) {
				this.selectedTeeth.push(id)
			}
			else{
				this.selectedTeeth =  this.selectedTeeth.filter((item) => item != id)
			}
		},
	},
	mounted() {
		this.teeth();
	},
};
